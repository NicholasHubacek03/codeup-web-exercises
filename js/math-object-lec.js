// will round decimal to nearest whole number
console.log(Math.round(6.96));

// will round decimal to nearest whole number downward
console.log(Math.floor(6.96));

// will round decimal to nearest whole number upward
console.log(Math.ceil(3.24))

// will return the value of base number to the power the exponent argument
// first argument will be base, second will be exponent
// ex: Math.pow(base, exponent)
console.log(Math.pow(3.16, 8));

// will return square root of number being passed in as the argument
console.log(Math.sqrt(81));
